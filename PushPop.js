const countries = ["Brazil", "China", "Denmark"];

countries.push("England"); // push adds an item to the end of the array
console.log(countries);

countries.pop(); //removes the last item of an array
console.log(countries);

countries.shift(); //shift removes first item of an array
console.log(countries);

countries.unshift("Nepal"); //unshift adds an item to the first in dex of an array.
console.log(countries);
